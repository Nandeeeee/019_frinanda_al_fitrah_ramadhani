# judul penugasan
print("Mengonversi suhu dalam derajat Celcius menjadi derajat Fahrenhait")

# mengambil input suhu celcius dari user
celcius = int(input("Masukkan suhu Celcius : "))

# mengonversi celcius ke fahrenhait
fahrenhait = celcius*9/5+32

# menampilkan hasil konversi
print("Hasil konversi ke Fahrenhait adalah", fahrenhait)