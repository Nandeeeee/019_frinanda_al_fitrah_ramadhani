print("Mengonversi suhu dalam derajat Fahrnhait menjadi derajat Celcius")

# mengambil input suhu fahrenhait dari user
fahrenhait = int(input("Masukkan suhu Fahrenhait : "))

# mengonversi fahrenhait ke celcius
celcius = (fahrenhait-32)*5/9

# menampilkan hasil konversi
print("Hasil konversi ke Celcius adalah", celcius)
