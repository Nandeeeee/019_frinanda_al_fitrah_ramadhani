# judul penugasan
print("Mengonversi suhu dalam derajat Kelvin menjadi derajat Celcius")

# mengambil input suhu kelvin dari user
kelvin = int(input("Masukkan suhu Kelvin : "))

# mengonversi kelvin ke celcius
celcius = kelvin-273.15

# menampilkan hasil konversi
print("Hasil konversi ke Celcius adalah", celcius)
